/* This is a multi-line comment
   which can span multiple lines
   and is useful for longer explanations or documentation. */

console.log("5"); // prints the number 5
console.log(5);   // prints the number 5
console.log(5 + 5); // prints the result of the addition, which is 10
console.log(5 - 2); // prints the result of the subtraction, which is 3
console.log(5 * 2); // prints the result of the multiplication, which is 10
console.log(5 / 2);  // prints the result of the division, which is 2.5
console.log(5 % 2);  // prints the remainder of the division, which is 1
console.log(5 ** 2); // prints the result of 5 raised to the power of 2, which is 25

console.log("5"+"5"); // prints the concatenation of the two strings, which is "55"

